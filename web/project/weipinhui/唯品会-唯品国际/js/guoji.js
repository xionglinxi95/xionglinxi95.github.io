$(function(){
    $(".guojia ul li").mouseenter(function(){
        $(this).css("opacity",1);
        $(this).siblings().css("opacity",0.5);
    });
    $(".guojia").mouseleave(function(){
        $(".guojia ul li").css("opacity",1);
    })
});


